const LangManager = {
    init() {
        this.loadSavedLang();
        this.updatePage();
        this.setupLangToggle();
    },

    loadSavedLang() {
        const savedLang = localStorage.getItem('lang');
        this.currentLang = savedLang || 'en';
        this.setLang(this.currentLang);
    },

    setLang(lang) {
        this.currentLang = lang;
        localStorage.setItem('lang', lang);

        // Update HTML dir and lang attributes
        document.documentElement.lang = lang;
        document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

        if (lang === 'ar') {
            document.body.classList.add('font-cairo');
        } else {
            document.body.classList.remove('font-cairo');
        }

        this.updatePage();
    },

    toggle() {
        const newLang = this.currentLang === 'en' ? 'ar' : 'en';
        this.setLang(newLang);
    },

    updatePage() {
        // Update text content
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (translations[this.currentLang][key]) {
                element.textContent = translations[this.currentLang][key];
            }
        });

        // Update placeholders
        document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
            const key = element.getAttribute('data-i18n-placeholder');
            if (translations[this.currentLang][key]) {
                element.placeholder = translations[this.currentLang][key];
            }
        });

        // Update Toggle Button Text/Icon if needed
        const btn = document.querySelector('[data-lang-toggle]');
        if (btn) {
            btn.innerHTML = this.currentLang === 'en' ? 'EN' : 'AR'; // Simple text toggle
            // Or update with flags/icons
            if (this.currentLang === 'en') {
                btn.textContent = "العربية";
                btn.classList.add('font-cairo'); // Optional: add Arabic font class
            } else {
                btn.textContent = "English";
                btn.classList.remove('font-cairo');
            }
        }
    }
};

document.addEventListener('DOMContentLoaded', () => {
    LangManager.init();
});
